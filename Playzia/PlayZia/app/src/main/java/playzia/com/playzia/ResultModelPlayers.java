package playzia.com.playzia;

public class ResultModelPlayers {
    String playerName;
    String playerWinning;
    String playerKill;

    public ResultModelPlayers() {

    }

    public String getPlayerName() {
        return playerName;
    }

    public void setPlayerName(String playerName) {
        this.playerName = playerName;
    }

    public String getPlayerWinning() {
        return playerWinning;
    }

    public void setPlayerWinning(String playerWinning) {
        this.playerWinning = playerWinning;
    }

    public String getPlayerKill() {
        return playerKill;
    }

    public void setPlayerKill(String playerKill) {
        this.playerKill = playerKill;
    }
}
